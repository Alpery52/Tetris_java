package no.uib.inf101.grid;

/**
 * The GridDimension interface represents the dimensions of a grid.
 *
 *
 */
public interface GridDimension {

  /**
   * Retrieves the number of rows in the grid.
   *
   * @return The number of rows in the grid.
   */
  int rows();

  /**
   * Retrieves the number of coloums in the grid.
   *
   * @return The number of cols in the grid.
   */
  int cols();
}
