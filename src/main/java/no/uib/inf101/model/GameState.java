package no.uib.inf101.model;

/**
 * The GameState enum represents the possible states of a game.
 * @author  Alper.Yarenbasi
 */
public enum GameState {
    /**
     * The game state for an active game
     */
    ACTIVE_GAME,
    /**
     * The game state when game is over
     */
    GAME_OVER,
    /**
     * The game state when first loading in to game / start screen
     */
    WELCOME_SCREEN
}
